#include "libft.h"

/**
 * @brief Counts the number of elements in a linked list.
 *
 * @param lst The pointer to the first element of the list.
 * @return The number of elements in the list.
 */
int	ft_lstsize(t_list *lst)
{
	int	count;

	count = 0;
	while (lst)
	{
		count++;
		lst = lst->next;
	}
	return (count);
}
